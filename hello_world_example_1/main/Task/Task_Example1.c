#include <stdint.h>
#include "freertos/FreeRTOS.h"
#include "freertos/FreeRTOSConfig.h"
#include "freertos/task.h"
#include "freertos/queue.h"
#include "esp_log.h"
#include "Task_Example1.h"

/* static functions */
static void Task_Example1( void * pParameters );

/*! This function initialize the task example 1
 *
 */
void Task_Example1_Init( void )
{
	TaskHandle_t taskHandlerConsole = NULL;

	xTaskCreate( Task_Example1, 							\
				 "Task_Example1",							\
				 configMINIMAL_STACK_SIZE * 5,				\
				 NULL, 										\
				 TASK_EXAMPLE1_TASK_PRIORITY, 				\
				 taskHandlerConsole);

}

/*! This function implements the task console
 * @param[in] pParameters Pointer to a struct of the requiered data
 * for this function, if necessary
 */
static void Task_Example1( void * pParameters )
{

	while(1)
	{

		printf("Task Example 1 says: Hello world!\n");

		vTaskDelay( 500/portTICK_PERIOD_MS );
	}


}
